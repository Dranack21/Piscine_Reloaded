int	ft_iterative_factorial(int nb)
{
	int	n;
	int	i;

	i = 1;
	n = 1;
	if (nb < 0)
		return (0);
	if (nb > 12)
		return (0);
	while (n <= nb)
	{
		i = i * n;
		n++;
	}
	return (i);
}
